#include <stdio.h>

int main() {
    // Khai bao va khoi tao 2 bien so nguyen
    int a = 10;      // Bien a co gia tri 10
    int b = 20;      // Bien b co gia tri 20

    // Tinh tong cua a va b, luu vao bien tong
    int tong = a + b;

    // In ra ket qua
    printf("Tong cua %d va %d la %d\n", a, b, tong);

    return 0;
}

